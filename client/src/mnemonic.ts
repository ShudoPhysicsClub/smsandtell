async function sha256(data: Uint8Array): Promise<Uint8Array> {
  const buf = await crypto.subtle.digest('SHA-256', data as unknown as BufferSource);
  return new Uint8Array(buf);
}

async function entropyToMnemonic(entropy: Uint8Array): Promise<string> {
  const hash = await sha256(entropy);
  const checksumByte = hash[0] ?? 0;

  // 256 bits entropy + 8 bits checksum = 264 bits = 24 × 11
  const bits: number[] = [];
  for (const byte of entropy) {
    for (let i = 7; i >= 0; i--) {
      bits.push((byte >> i) & 1);
    }
  }
  for (let i = 7; i >= 0; i--) {
    bits.push((checksumByte >> i) & 1);
  }

  const words: string[] = [];
  for (let i = 0; i < 24; i++) {
    let idx = 0;
    for (let j = 0; j < 11; j++) {
      idx = (idx << 1) | (bits[i * 11 + j] ?? 0);
    }
    words.push(WORDLIST[idx] ?? '');
  }
  return words.join(' ');
}

/** 32バイトのエントロピーからランダムな秘密鍵を生成し、対応するBIP39ニーモニック（24語）と16進文字列を返す */
export async function generateMnemonic(): Promise<{ privateKeyHex: string; mnemonic: string }> {
  const entropy = new Uint8Array(32);
  crypto.getRandomValues(entropy);
  const mnemonic = await entropyToMnemonic(entropy);
  const privateKeyHex = Array.from(entropy)
    .map((b) => b.toString(16).padStart(2, '0'))
    .join('')
    .toUpperCase();
  return { privateKeyHex, mnemonic };
}

/**
 * BIP39ニーモニック（24語）を秘密鍵の16進文字列に変換する。
 * チェックサムの検証も行う。
 */
export async function mnemonicToPrivateKeyHex(mnemonic: string): Promise<string> {
  const words = mnemonic.trim().toLowerCase().split(/\s+/);
  if (words.length !== 24) {
    throw new Error(`ニーモニックは24単語必要です（現在: ${words.length}単語）`);
  }

  const bits: number[] = [];
  for (const word of words) {
    const idx = WORDLIST.indexOf(word);
    if (idx === -1) throw new Error(`無効な単語: "${word}"`);
    for (let i = 10; i >= 0; i--) {
      bits.push((idx >> i) & 1);
    }
  }

  const entropy = new Uint8Array(32);
  for (let i = 0; i < 32; i++) {
    let byte = 0;
    for (let j = 0; j < 8; j++) {
      byte = (byte << 1) | (bits[i * 8 + j] ?? 0);
    }
    entropy[i] = byte;
  }

  // チェックサム検証
  const hash = await sha256(entropy);
  const expectedChecksum = hash[0] ?? 0;
  let actualChecksum = 0;
  for (let i = 0; i < 8; i++) {
    actualChecksum = (actualChecksum << 1) | (bits[256 + i] ?? 0);
  }
  if (expectedChecksum !== actualChecksum) {
    throw new Error('ニーモニックのチェックサムが一致しません。単語を確認してください。');
  }

  return Array.from(entropy)
    .map((b) => b.toString(16).padStart(2, '0'))
    .join('')
    .toUpperCase();
}
